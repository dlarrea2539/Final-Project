let sq1 = document.querySelector('.square1')
    console.log(sq1)
        sq1.addEventListener("mouseover", e =>{
            sq1.classList.add('rotate')
            sq1.style.backgroundColor = "lightblue"
            setTimeout(e => {
                sq1.style.backgroundColor = "darkblue"
            },2000)
            setTimeout(e => {
            sq1.classList.remove('rotate')
        },3000)
        })


